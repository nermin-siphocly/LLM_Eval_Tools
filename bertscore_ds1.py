from bert_score import score
import pandas as pd

try:
    df = pd.read_excel('QA.xlsx')
except FileNotFoundError:
    print("Error: Excel file not found. Please make sure the path is correct and the file exists.")
    # Exit or handle the error appropriately
    exit()

# Assuming 'Column B' and 'Column C' are the names of your columns
references = df['Model Answer'].tolist()
candidates = df['Deepseek'].tolist()#df['ChatGPT'].tolist()#df['Grok'].tolist()

P, R, F1 = score(candidates, references, lang="en", verbose=True)

print(f"Precision: {P.mean():.4f}")
print(f"Recall: {R.mean():.4f}")
print(f"F1: {F1.mean():.4f}")

# Create a DataFrame with the scores
scores_df = pd.DataFrame({'Precision': P.tolist(), 'Recall': R.tolist(), 'F1': F1.tolist()})

# Read the existing Excel file
df = pd.read_excel('QA.xlsx')  # Replace 'QA.xlsx' with your actual file name

# Concatenate the scores with the original DataFrame
df = pd.concat([df, scores_df], axis=1)

# Write the updated DataFrame back to Excel
df.to_excel('QA_Bert_Deepseek.xlsx', index=False) #QA_Bert_GPT.xlsx #QA_Bert_Grok.xlsx