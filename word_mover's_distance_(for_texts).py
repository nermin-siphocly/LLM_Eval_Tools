from gensim.models import KeyedVectors
from gensim.utils import simple_preprocess
from gensim.corpora.dictionary import Dictionary
import numpy as np

# Load pre-trained Word2Vec model (GoogleNews-vectors-negative300)
# Download from: https://s3.amazonaws.com/dl4j-distribution/GoogleNews-vectors-negative300.bin.gz
model_path = "/kaggle/input/googlenewsvectorsnegative300/GoogleNews-vectors-negative300.bin"
word_vectors = KeyedVectors.load_word2vec_format(model_path, binary=True)

# Preprocess text
def preprocess(text):
    return simple_preprocess(text)

# Compute Word Mover's Distance
def compute_wmd(text1, text2, model):
    tokens1 = preprocess(text1)
    tokens2 = preprocess(text2)

    # Filter tokens not in the model vocab
    tokens1 = [token for token in tokens1 if token in model]
    tokens2 = [token for token in tokens2 if token in model]

    # If no overlapping vocab, return max distance
    if not tokens1 or not tokens2:
        return float('inf')

     # Compute raw WMD
    raw_wmd = model.wmdistance(tokens1, tokens2)

    # Normalize by average length
    avg_len = (len(tokens1) + len(tokens2)) / 2
    normalized_wmd = raw_wmd / avg_len if avg_len > 0 else float('inf')

    return normalized_wmd

