# STEP 3: Load model and scoring function
from sentence_transformers import SentenceTransformer, util

model = SentenceTransformer('all-MiniLM-L6-v2')

def score_semantic_similarity(reference: str, prediction: str) -> float:
    emb1 = model.encode(reference, convert_to_tensor=True)
    emb2 = model.encode(prediction, convert_to_tensor=True)
    return util.pytorch_cos_sim(emb1, emb2).item()

import re

# STEP 4: Load test suite manually and compute scores
def process():
  with open("test_suite.json", "r") as f:
      suite = json.load(f)

  results = []
  for example_index, example in enumerate(suite):
      references = example["reference_output"]
      for model_name, predictions in example["responses"].items():
          for i in range(len(references)):
              reference = references[i]
              reference = re.sub(r'[^a-zA-Z0-9.;,!?/\|@&#$-_ ]', '', reference)
              prediction = predictions[i]
              prediction = re.sub(r'[^a-zA-Z0-9.;,!?/\|@&#$-_ ]', '', prediction)
              score = score_semantic_similarity(reference, prediction)
              results.append({
                  "prompt_index":  example_index + i,
                  "model": model_name,
                  "semantic_score": round(score, 3)
              })
  return results

# Create a DataFrame with the scores
scores_df = pd.DataFrame(process())
