import pandas as pd
try:
    df = pd.read_excel('KC.xlsx')
except FileNotFoundError:
    print("Error: Excel file not found. Please make sure the path is correct and the file exists.")
    # Exit or handle the error appropriately
    exit()

references = df['Conclusion'].tolist()
gptRes = df['GPT'].tolist()
grokRes = df['Grok'].tolist()
deepSeekRes = df['DeepSeek'].tolist()

import re

for i in range(len(references)):
  references[i] = re.sub(r'[^a-zA-Z0-9.;,!?/\|@&#$ \n]', '', references[i])
for i in range(len(grokRes)):
  grokRes[i] = re.sub(r'[^a-zA-Z0-9.;,!?/\|@&#$ \n]', '', grokRes[i])
for i in range(len(gptRes)):
  gptRes[i] = re.sub(r'[^a-zA-Z0-9.;,!?/\|@&#$ \n]', '', gptRes[i])
for i in range(len(deepSeekRes)):
  deepSeekRes[i] = re.sub(r'[^a-zA-Z0-9.;,!?/\|@&#$ \n]', '', deepSeekRes[i])

"""# Keybert

"""

from keybert import KeyBERT
from sentence_transformers import SentenceTransformer

model = SentenceTransformer("paraphrase-multilingual-mpnet-base-v2")
kw_model = KeyBERT(model=model)

references_keywords = []
gpt_keywords = []
grok_keywords = []
deepseek_keywords = []

for i in range(len(references)):
    keywords_with_scores = kw_model.extract_keywords(references[i], keyphrase_ngram_range=(1, 3), stop_words='english',
                                  use_maxsum=True, nr_candidates=20, top_n=10)
    keywords_only = [keyword for keyword, score in keywords_with_scores]
    references_keywords.append(keywords_only)

    keywords_with_scores = kw_model.extract_keywords(gptRes[i], keyphrase_ngram_range=(1, 3), stop_words='english',
                                  use_maxsum=True, nr_candidates=20, top_n=10)
    keywords_only = [keyword for keyword, score in keywords_with_scores]
    gpt_keywords.append(keywords_only)

    keywords_with_scores = kw_model.extract_keywords(grokRes[i], keyphrase_ngram_range=(1, 3), stop_words='english',
                                  use_maxsum=True, nr_candidates=20, top_n=10)
    keywords_only = [keyword for keyword, score in keywords_with_scores]
    grok_keywords.append(keywords_only)

    keywords_with_scores = kw_model.extract_keywords(deepSeekRes[i], keyphrase_ngram_range=(1, 3), stop_words='english',
                                  use_maxsum=True, nr_candidates=20, top_n=10)
    keywords_only = [keyword for keyword, score in keywords_with_scores]
    deepseek_keywords.append(keywords_only)

# Create a dictionary with the keyword lists
data = {
    'References Keywords': references_keywords,
    'GPT Keywords': gpt_keywords,
    'Grok Keywords': grok_keywords,
    'DeepSeek Keywords': deepseek_keywords
}

# Create the DataFrame
df_keywords = pd.DataFrame(data)

# Save the DataFrame to an Excel file
df_keywords.to_excel('keybert_maxsum.xlsx', index=False)

