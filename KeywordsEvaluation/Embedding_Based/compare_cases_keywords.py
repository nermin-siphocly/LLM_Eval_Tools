from sentence_transformers import SentenceTransformer
from sklearn.metrics.pairwise import cosine_similarity

model = SentenceTransformer('all-MiniLM-L6-v2') # Using a general purpose model

import pandas as pd

def clean_keywords(keyword_str):
    return [s.strip().replace("['", "").replace("']", "").replace("'", "") for s in keyword_str.split(',')]

def compute_scores(references, predictions, model, threshold=0.8, word_overlap_threshold=2):
    exact_scores, semantic_scores, partial_scores = [], [], []

    for ref, pred in zip(references, predictions):
        set2 = clean_keywords(ref)
        set1 = clean_keywords(pred)

        # Exact Match
        exact_count = sum(1 for expr in set2 if expr in set1)
        exact_score = exact_count / len(set2) if set2 else 0
        exact_scores.append(exact_score)

        # Semantic Match
        embeddings1 = model.encode(set1)
        embeddings2 = model.encode(set2)
        sem_count = 0
        for i, expr2 in enumerate(set2):
            for j, expr1 in enumerate(set1):
                similarity = cosine_similarity([embeddings2[i]], [embeddings1[j]])[0][0]
                if similarity >= threshold:
                    sem_count += 1
                    break
        semantic_scores.append(sem_count / len(set2) if set2 else 0)

        # Partial Match
        partial_count = 0
        for expr2 in set2:
            words2 = set(expr2.split())
            for expr1 in set1:
                words1 = set(expr1.split())
                if len(words1.intersection(words2)) >= word_overlap_threshold:
                    partial_count += 1
                    break
        partial_scores.append(partial_count / len(set2) if set2 else 0)

    return exact_scores, semantic_scores, partial_scores

def load_and_score(file_path, model, llm_name):
    try:
        df = pd.read_excel(file_path)
    except FileNotFoundError:
        print(f"Error: {file_path} not found. Please make sure the path is correct and the file exists.")
        exit()

    refs = df['References Keywords'].tolist()
    llm = df[llm_name+' Keywords'].tolist()
    return compute_scores(refs, llm, model)


def processing(file_sources, llm_name, output_file_prefix, dframe, comp_name):
  for file, prefix in file_sources.items():
    ex, sem, part = load_and_score(file, model, llm_name)
    df_keywords[f'{prefix}_exact_score'] = ex
    df_keywords[f'{prefix}_semantic_score'] = sem
    df_keywords[f'{prefix}_partial_score'] = part

  # Save the DataFrame to an Excel file
  dframe.to_excel(output_file_prefix+'_'+comp_name+'.xlsx', index=False)

df_keywords = pd.DataFrame()

