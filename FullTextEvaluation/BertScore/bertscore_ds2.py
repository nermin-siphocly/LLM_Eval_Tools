from bert_score import score
import pandas as pd

try:
    df = pd.read_excel('KC.xlsx')
    #df = pd.read_excel('QA.xlsx')
except FileNotFoundError:
    print("Error: Excel file not found. Please make sure the path is correct and the file exists.")
    # Exit or handle the error appropriately
    exit()

# Assuming 'Column B' and 'Column C' are the names of your columns
references = df['Conclusion'].tolist()#'Model Answer'
candidates = df['Deepseek'].tolist()#df['ChatGPT'].tolist()#df['Grok'].tolist()#df['Deepseek'].tolist()

P, R, F1 = score(candidates, references, lang="en", verbose=True)

print(f"Precision: {P.mean():.4f}")
print(f"Recall: {R.mean():.4f}")
print(f"F1: {F1.mean():.4f}")

# Create a DataFrame with the scores
LLM_name = 'Deepseek' #'GPT' #'Grok' #'Deepseek'
df = pd.DataFrame({LLM_name + ' Precision': P.tolist(), LLM_name + 'Recall': R.tolist(), LLM_name + 'F1': F1.tolist()})


# Write the updated DataFrame back to Excel
df.to_excel('KC_Bert_Deepseek.xlsx', index=False) #QA_Bert_GPT.xlsx #QA_Bert_Grok.xlsx #QA_Bert_Deepseek.xlsx
