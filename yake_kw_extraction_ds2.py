# -------------------------
# Imports
import pandas as pd
import re
import yake

# Load your Excel file
try:
    df = pd.read_excel('KC.xlsx')
except FileNotFoundError:
    print("Error: Excel file not found. Please make sure the path is correct and the file exists.")
    exit()

# -------------------------
# Prepare the text lists
references = df['Conclusion'].tolist()
gptRes = df['ChatGPT'].tolist()
grokRes = df['Grok'].tolist()
deepSeekRes = df['Deepseek'].tolist()

# -------------------------
# Clean the texts
def clean_text_list(text_list):
    cleaned_list = []
    for text in text_list:
        cleaned_text = re.sub(r'[^a-zA-Z0-9.;,!?/\|@&#$ \n]', '', str(text))
        cleaned_list.append(cleaned_text)
    return cleaned_list

references = clean_text_list(references)
gptRes = clean_text_list(gptRes)
grokRes = clean_text_list(grokRes)
deepSeekRes = clean_text_list(deepSeekRes)

# -------------------------
# YAKE keyword extraction
kw_extractor = yake.KeywordExtractor(lan="en", n=3, top=10)  # extract up to 3-word phrases

def extract_keywords(text):
    try:
        keywords_with_scores = kw_extractor.extract_keywords(text)
        keywords_only = [kw for kw, score in keywords_with_scores]
        return keywords_only
    except:
        return []

# -------------------------
# Generate keywords for each list
references_keywords = [extract_keywords(text) for text in references]
gpt_keywords = [extract_keywords(text) for text in gptRes]
grok_keywords = [extract_keywords(text) for text in grokRes]
deepseek_keywords = [extract_keywords(text) for text in deepSeekRes]

# -------------------------
# Save to Excel
data = {
    'References Keywords': references_keywords,
    'GPT Keywords': gpt_keywords,
    'Grok Keywords': grok_keywords,
    'DeepSeek Keywords': deepseek_keywords
}

df_keywords = pd.DataFrame(data)
df_keywords.to_excel('yake_keywords.xlsx', index=False)

print("Finished extracting keywords with YAKE. Saved to 'yake_keywords.xlsx'")