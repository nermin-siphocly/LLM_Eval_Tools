def clean_keywords(keyword_str):
    return [s.strip().replace("['", "").replace("']", "").replace("'", "") for s in keyword_str.split(',')]

from sklearn.metrics.pairwise import cosine_similarity
import numpy as np

def compute_scores(references, predictions, semantic_model, wmd_model, threshold=0.8, word_overlap_threshold=2):
    exact_scores, semantic_scores, avg_emb, partial_scores, wmd_scores = [], [], [], [], []

    for ref, pred in zip(references, predictions):
        set2 = ref
        set1 = pred

        # Exact Match
        exact_count = sum(1 for expr in set2 if expr in set1)
        exact_score = exact_count / len(set2) if set2 else 0
        exact_scores.append(exact_score)

        # Semantic Match
        embeddings1 = semantic_model.encode(set1)
        embeddings2 = semantic_model.encode(set2)

        sem_count = 0
        for i, expr2 in enumerate(set2):
            for j, expr1 in enumerate(set1):
                similarity = cosine_similarity([embeddings2[i]], [embeddings1[j]])[0][0]
                if similarity >= threshold:
                    sem_count += 1
                    break
        semantic_scores.append(sem_count / len(set2) if set2 else 0)

        # Get embeddings for each keyphrase
        embeddings1 = model.encode(set1)
        embeddings2 = model.encode(set2)

        # Compute average embedding of each set
        avg_embedding1 = np.mean(embeddings1, axis=0)
        avg_embedding2 = np.mean(embeddings2, axis=0)

        # Compute cosine similarity between average embeddings
        avg_emb.append(cosine_similarity([avg_embedding1], [avg_embedding2])[0][0])

        # Partial Match
        partial_count = 0
        for expr2 in set2:
            words2 = set(expr2.split())
            for expr1 in set1:
                words1 = set(expr1.split())
                if len(words1.intersection(words2)) >= word_overlap_threshold:
                    partial_count += 1
                    break
        partial_scores.append(partial_count / len(set2) if set2 else 0)

        # Word Mover's Distance
        words1 = [word for phrase in set1 for word in phrase.lower().split()]
        words2 = [word for phrase in set2 for word in phrase.lower().split()]
        try:
            distance = wmd_model.wmdistance(words1, words2)
        except:
            distance = float('inf')
        wmd_scores.append(distance)

    return exact_scores, semantic_scores, avg_emb, partial_scores, wmd_scores

